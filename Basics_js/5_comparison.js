
// Comparison Operators

// Equal to (==)
console.log(5 == '5'); // true

// Not equal to (!=)
console.log(5 != '5'); // false

// Strict equal to (===)
console.log(5 === '5'); // false

// Strict not equal to (!==)
console.log(5 !== '5'); // true 

// Greater than (>)
console.log(5 > 3); // true

// Less than (<)    
console.log(5 < 3); // false

// Greater than or equal to (>=)            
console.log(5 >= 5); // true

// Less than or equal to (<=)            
console.log(5 <= 3); // false

